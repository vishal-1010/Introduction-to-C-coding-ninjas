#include <iostream>
using namespace std;

int main(){
    int n;
    cin >> n;
    int i = 1;
    while (i <= n){
        int c = cin.get();
        int c1;
        if (c > c1){
            cout << "false";
        }
        else{
            cout << "true";
        }
        c1 = c;
        i++;
    }
}